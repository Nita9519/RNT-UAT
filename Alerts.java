package ai.rnt.pins.model;

public class Alerts {

	String projectName;
	String companyName;
	String alertNDA;
	String alertMSA;
	String alertSOW;
	
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	
	public String getAlertNDA() {
		return alertNDA;
	}
	public void setAlertNDA(String alertNDA) {
		this.alertNDA = alertNDA;
	}
	public String getAlertMSA() {
		return alertMSA;
	}
	public void setAlertMSA(String alertMSA) {
		this.alertMSA = alertMSA;
	}
	public String getAlertSOW() {
		return alertSOW;
	}
	public void setAlertSOW(String alertSOW) {
		this.alertSOW = alertSOW;
	}
	
}
